double integrate_1_dim(double (*f)(double), double start, double end, int n){
    /* integrates a one dimensional real valued function f() from start to end
       using the Gaussian integration with n supporting points
    */
    int i;
    int nmax = (n-1)/2+1;
    int nMinusnmax = n-nmax;
    double *w, *x;
    double a_plus_b_half = 0.5*(start + end);
    double b_minus_a_half = 0.5*(end - start);
    double scaled_w, scaled_x;
    double d1, d = 0;

    w = new double[nmax];
    x = new double[nmax];

    get_weights_points(w, x, n);

    for (i=0; i<nmax; ++i){
        scaled_w = b_minus_a_half * w[i];
        scaled_x = a_plus_b_half + b_minus_a_half * x[i];
        d1 = f(scaled_x);

        if (i<nMinusnmax){
            scaled_x = a_plus_b_half - b_minus_a_half * x[i];
            d1 += f(scaled_x);
        }
        d += scaled_w * d1;
    }

    delete[] x;
    delete[] w;

    return(d);
}


double integrate_multi_dim(double (*f)(double[]), double *start, double *end, int dim, int n, double *farg, double *w, double *x){
    /* Integrates a real valued function f() in dim dimensions from start[dim] to end[dim]
       using the Gaussian integration with n supporting points
       The algorithm uses an recursive algorithm, due to this, the optimal arguments
       farg, w, x are present, so that the weights and supporting points do not needed to be 
       generated during every recursive function call.
    */
    int i;
    int nmax = (n-1)/2+1;
    int nMinusnmax = n-nmax;
    double a_plus_b_half = 0.5*(start[dim-1] + end[dim-1]);
    double b_minus_a_half = 0.5*(end[dim-1] - start[dim-1]);
    double scaled_w, scaled_x;
    double d1,d=0;
    bool wx_generated=false;

    if (w==nullptr && x==nullptr && farg==nullptr){
        farg = new double[dim];
        w = new double[n];
        x = new double[n];
        get_weights_points(w, x, n);
        wx_generated = true;
    }

    for (i=0; i<nmax; ++i){
        scaled_w = b_minus_a_half * w[i];
        farg[dim-1] = a_plus_b_half + b_minus_a_half * x[i];
        if (dim > 1) {
            d1 = integrate_multi_dim(f, start, end, dim-1, n, farg, w, x);
        } else {
            d1 = f(farg);
        }

        if (i<nMinusnmax){
            farg[dim-1] = a_plus_b_half - b_minus_a_half * x[i];
            if (dim > 1){
                d1 += integrate_multi_dim(f, start, end, dim-1, n, farg, w, x);
            } else {
                d1 += f(farg);
            }
        }
        d += scaled_w * d1;
    }

    if (wx_generated){
        delete[] farg;
        delete[] w;
        delete[] x;
    }

    return d;
}


